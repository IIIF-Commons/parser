import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { join, relative, resolve } from "node:path";
import { normalize, serialize, serializeConfigPresentation4 } from "../dist/presentation-4.js";

const repoRoot = process.cwd();
const fixtureRoot = resolve(repoRoot, "fixtures");
const fixtureDirs = [resolve(fixtureRoot, "presentation-4"), resolve(fixtureRoot, "cookbook-v4")];
const outputFile = resolve(repoRoot, "__tests__", "presentation-types", "p4-fixture-manifest-types.test.ts");

function toPosixPath(value) {
  return value.split("\\").join("/");
}

function walkJsonFiles(dir) {
  const files = [];
  const entries = readdirSync(dir).sort();
  for (const entry of entries) {
    const fullPath = join(dir, entry);
    const stats = statSync(fullPath);
    if (stats.isDirectory()) {
      files.push(...walkJsonFiles(fullPath));
      continue;
    }
    if (stats.isFile() && entry.endsWith(".json") && entry !== "_index.json") {
      files.push(fullPath);
    }
  }
  return files;
}

function toFixtureKey(pathname) {
  return pathname
    .replace(/\.json$/i, "")
    .replace(/[^a-zA-Z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .toLowerCase();
}

function toObjectLiteral(value, indent = 4) {
  const padding = " ".repeat(indent);
  return JSON.stringify(value, null, 2)
    .split("\n")
    .map((line) => `${padding}${line}`)
    .join("\n");
}

function main() {
  const files = fixtureDirs.flatMap((dir) => walkJsonFiles(dir)).sort();
  const lines = [];
  const seenKeys = new Set();
  let manifestCount = 0;

  lines.push("import { describe, expect, test } from 'vitest';");
  lines.push("import type { Manifest as Manifest4 } from '../../src/presentation-4/types';");
  lines.push("");
  lines.push(
    "// Generated by scripts/generate-p4-fixture-type-test.mjs from fixtures/presentation-4 and fixtures/cookbook-v4."
  );
  lines.push("const manifestsByFixture = {");

  for (const filePath of files) {
    const inputJson = JSON.parse(readFileSync(filePath, "utf8"));
    if (!inputJson || typeof inputJson !== "object" || typeof inputJson.type !== "string") {
      continue;
    }

    const normalized = normalize(inputJson);
    const serialized = serialize(
      {
        entities: normalized.entities,
        mapping: normalized.mapping,
        requests: {},
      },
      normalized.resource,
      serializeConfigPresentation4
    );

    if (!serialized || typeof serialized !== "object" || serialized.type !== "Manifest") {
      continue;
    }

    const relPath = toPosixPath(relative(fixtureRoot, filePath));
    const baseKey = toFixtureKey(relPath);
    let fixtureKey = baseKey;
    let keyCounter = 2;
    while (seenKeys.has(fixtureKey)) {
      fixtureKey = `${baseKey}_${keyCounter}`;
      keyCounter += 1;
    }
    seenKeys.add(fixtureKey);
    manifestCount += 1;

    lines.push(`  ${JSON.stringify(fixtureKey)}: ${toObjectLiteral(serialized)} satisfies Manifest4,`);
  }

  lines.push("} as const;");
  lines.push("");
  lines.push("describe('presentation-4 fixture manifest typing', () => {");
  lines.push("  test('all fixture manifests satisfy Manifest4', () => {");
  lines.push(`    expect(Object.keys(manifestsByFixture).length).toBe(${manifestCount});`);
  lines.push("  });");
  lines.push("});");
  lines.push("");

  writeFileSync(outputFile, lines.join("\n"));
  console.log(`Generated ${toPosixPath(relative(repoRoot, outputFile))}`);
}

main();
