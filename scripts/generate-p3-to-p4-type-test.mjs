import { spawnSync } from 'node:child_process';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join, resolve } from 'node:path';

const convertedRoot = resolve(process.cwd(), 'fixtures', '3-to-4-converted');
const convertedIndex = join(convertedRoot, '_index.json');
const outputFile = resolve(process.cwd(), '__tests__', 'presentation-types', 'p4-converted-from-p3-manifest-types.test.ts');

function toFixtureKey(pathname) {
  return pathname.replace(/\.json$/i, '').replace(/[^a-zA-Z0-9]+/g, '_').replace(/^_+|_+$/g, '').toLowerCase();
}

function toObjectLiteral(value, indent = 4) {
  const padding = ' '.repeat(indent);
  return JSON.stringify(value, null, 2)
    .split('\n')
    .map((line) => `${padding}${line}`)
    .join('\n');
}

function loadConvertedManifestFiles() {
  if (!existsSync(convertedIndex)) {
    throw new Error('fixtures/3-to-4-converted/_index.json does not exist. Run convert-p3-manifests-to-p4 first.');
  }

  const indexJson = JSON.parse(readFileSync(convertedIndex, 'utf8'));
  if (!Array.isArray(indexJson)) {
    throw new Error('fixtures/3-to-4-converted/_index.json must be an array of relative JSON paths.');
  }

  return indexJson.filter((item) => typeof item === 'string' && item.endsWith('.json')).sort();
}

function generateTestSource(entries) {
  const lines = [];
  lines.push("import { describe, expect, test } from 'vitest';");
  lines.push("import type { Manifest as Manifest4 } from '../../src/presentation-4/types';");
  lines.push('');
  lines.push('// Generated by scripts/generate-p3-to-p4-type-test.mjs from fixtures/3-to-4-converted.');
  lines.push('const manifestsByFixture = {');

  const seenKeys = new Set();
  for (const entry of entries) {
    const manifestPath = join(convertedRoot, entry);
    const manifestJson = JSON.parse(readFileSync(manifestPath, 'utf8'));
    if (!manifestJson || typeof manifestJson !== 'object' || manifestJson.type !== 'Manifest') {
      continue;
    }

    const baseKey = toFixtureKey(entry);
    let key = baseKey;
    let index = 2;
    while (seenKeys.has(key)) {
      key = `${baseKey}_${index}`;
      index += 1;
    }
    seenKeys.add(key);

    lines.push(`  ${JSON.stringify(key)}: ${toObjectLiteral(manifestJson)} satisfies Manifest4,`);
  }

  lines.push('} as const;');
  lines.push('');
  lines.push("describe('presentation-4 upgraded-from-v3 fixture typing', () => {");
  lines.push("  test('all converted manifests satisfy Manifest4', () => {");
  lines.push('    expect(Object.keys(manifestsByFixture).length).toBeGreaterThan(0);');
  lines.push('  });');
  lines.push('});');
  lines.push('');

  return lines.join('\n');
}

function runLint() {
  const result = spawnSync('pnpm', ['run', 'lint'], {
    cwd: process.cwd(),
    stdio: 'inherit',
    shell: process.platform === 'win32',
  });

  if (typeof result.status === 'number' && result.status !== 0) {
    process.exit(result.status);
  }

  if (result.error) {
    throw result.error;
  }
}

function main() {
  const entries = loadConvertedManifestFiles();
  const testSource = generateTestSource(entries);
  writeFileSync(outputFile, testSource);
  console.log(`Generated ${outputFile}`);
  runLint();
}

main();
