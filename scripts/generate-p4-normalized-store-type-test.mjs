import { spawnSync } from 'node:child_process';
import { existsSync, readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { join, relative, resolve } from 'node:path';
import { normalize } from '../dist/presentation-4.js';

const repoRoot = process.cwd();
const fixturesRoot = resolve(repoRoot, 'fixtures');
const convertedRoot = resolve(fixturesRoot, '3-to-4-converted');
const convertedIndex = join(convertedRoot, '_index.json');
const outputFile = resolve(
  repoRoot,
  '__tests__',
  'presentation-types',
  'p4-normalized-store-types.test.ts'
);
const staticFixtureDirs = [resolve(fixturesRoot, 'presentation-4'), resolve(fixturesRoot, 'cookbook-v4')];
const canvasLikeTypes = new Set(['Canvas', 'Scene', 'Timeline']);
const excludedFixturePaths = new Set([
  'presentation-4/06-perspective-camera-with-choice.json',
]);

function toPosixPath(value) {
  return value.split('\\').join('/');
}

function walkJsonFiles(dir) {
  const entries = readdirSync(dir).sort();
  const files = [];

  for (const entry of entries) {
    const fullPath = join(dir, entry);
    const stats = statSync(fullPath);

    if (stats.isDirectory()) {
      files.push(...walkJsonFiles(fullPath));
      continue;
    }

    if (stats.isFile() && entry.endsWith('.json')) {
      files.push(fullPath);
    }
  }

  return files;
}

function getConvertedManifestFiles() {
  if (!existsSync(convertedIndex)) {
    return [];
  }

  const indexJson = JSON.parse(readFileSync(convertedIndex, 'utf8'));
  if (!Array.isArray(indexJson)) {
    return [];
  }

  return indexJson
    .filter((item) => typeof item === 'string' && item.endsWith('.json'))
    .map((item) => join(convertedRoot, item));
}

function isManifest(value) {
  return Boolean(value && typeof value === 'object' && value.type === 'Manifest');
}

function isCanvasLikeResource(value) {
  return Boolean(value && typeof value === 'object' && canvasLikeTypes.has(value.type));
}

function pruneToSingleCanvasPerResource(value) {
  if (Array.isArray(value)) {
    return value.map((item) => pruneToSingleCanvasPerResource(item));
  }

  if (!value || typeof value !== 'object') {
    return value;
  }

  const output = {};
  for (const [key, nested] of Object.entries(value)) {
    if (key !== 'items') {
      output[key] = pruneToSingleCanvasPerResource(nested);
      continue;
    }

    if (!Array.isArray(nested)) {
      output[key] = pruneToSingleCanvasPerResource(nested);
      continue;
    }

    const prunedItems = nested.map((item) => pruneToSingleCanvasPerResource(item));
    let canvasLikeSeen = false;
    output[key] = prunedItems.filter((item) => {
      if (!isCanvasLikeResource(item)) {
        return true;
      }
      if (canvasLikeSeen) {
        return false;
      }
      canvasLikeSeen = true;
      return true;
    });
  }

  return output;
}

function toFixtureKey(pathname) {
  return pathname.replace(/\.json$/i, '').replace(/[^a-zA-Z0-9]+/g, '_').replace(/^_+|_+$/g, '').toLowerCase();
}

function toObjectLiteral(value, indent = 4) {
  const padding = ' '.repeat(indent);
  return JSON.stringify(value, null, 2)
    .split('\n')
    .map((line) => `${padding}${line}`)
    .join('\n');
}

function loadManifestEntries() {
  const discovered = [];
  for (const fixtureDir of staticFixtureDirs) {
    if (!existsSync(fixtureDir)) {
      continue;
    }
    discovered.push(...walkJsonFiles(fixtureDir));
  }

  discovered.push(...getConvertedManifestFiles());

  const unique = [...new Set(discovered.map((filePath) => resolve(filePath)))];
  const filtered = unique.filter((filePath) => {
    const rel = toPosixPath(relative(fixturesRoot, filePath));
    return !excludedFixturePaths.has(rel);
  });
  filtered.sort();
  return filtered;
}

function generateTestSource(entries) {
  const lines = [];
  lines.push("import { describe, expect, test } from 'vitest';");
  lines.push("import type { Presentation4Entities } from '../../src/presentation-4-normalized/types';");
  lines.push('');
  lines.push(
    '// Generated by scripts/generate-p4-normalized-store-type-test.mjs from p4 fixtures and converted manifests.'
  );
  lines.push('const normalizedStoresByFixture = {');

  const seenKeys = new Set();
  let included = 0;
  for (const filePath of entries) {
    const fixtureJson = JSON.parse(readFileSync(filePath, 'utf8'));
    if (!isManifest(fixtureJson)) {
      continue;
    }

    const prunedManifest = pruneToSingleCanvasPerResource(fixtureJson);
    const normalized = normalize(prunedManifest);
    const fixtureKeyBase = toFixtureKey(toPosixPath(relative(fixturesRoot, filePath)));

    let fixtureKey = fixtureKeyBase;
    let keyCounter = 2;
    while (seenKeys.has(fixtureKey)) {
      fixtureKey = `${fixtureKeyBase}_${keyCounter}`;
      keyCounter += 1;
    }
    seenKeys.add(fixtureKey);
    included += 1;

    lines.push(`  ${JSON.stringify(fixtureKey)}: ${toObjectLiteral(normalized.entities)} satisfies Presentation4Entities,`);
  }

  lines.push('} as const;');
  lines.push('');
  lines.push("describe('presentation-4 normalized store typing', () => {");
  lines.push("  test('all normalized stores satisfy Presentation4Entities', () => {");
  lines.push(`    expect(Object.keys(normalizedStoresByFixture).length).toBe(${included});`);
  lines.push('  });');
  lines.push('});');
  lines.push('');

  return lines.join('\n');
}

function runLint() {
  const result = spawnSync('pnpm', ['run', 'lint'], {
    cwd: repoRoot,
    stdio: 'inherit',
    shell: process.platform === 'win32',
  });

  if (typeof result.status === 'number' && result.status !== 0) {
    process.exit(result.status);
  }

  if (result.error) {
    throw result.error;
  }
}

function main() {
  const entries = loadManifestEntries();
  const testSource = generateTestSource(entries);
  writeFileSync(outputFile, testSource);
  console.log(`Generated ${toPosixPath(relative(repoRoot, outputFile))}`);
  runLint();
}

main();
